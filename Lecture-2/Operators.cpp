// Operators.cpp
#include <iostream>
using namespace std;

int main() {

	int a, b;
	cin >> a >> b;

	cout << "Sum: " << a + b << endl;

	int diff = a - b;
	cout << "Difference: " << diff << endl;

	int rem = a % 2;
	cout << "Remainder of " << a << " and 2: " << rem << endl;

	return 0;
}
















